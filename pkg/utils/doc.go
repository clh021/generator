// Package utils provides utility functions for the generator package.
//
// This package contains auxiliary functions that are not part of the core
// generator functionality but can be useful when building applications
// that use the generator package.
//
// The utilities are organized into categories:
//
// File System Utilities (fs_scanner.go):
//   - Scanning directories for YAML files
//   - Finding common configuration files
//
// Command Line Interface Utilities (cli_ui.go):
//   - Displaying file lists to users
//   - Getting user selections from the command line
//
// These utilities are designed to be used with the generator package
// but can also be used independently in other applications.
package utils
